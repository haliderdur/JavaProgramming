package day19_LoopPractises;

public class LoopsWithoutCurlyBraces {
    public static void main(String[] args) {

// Without curly braces, loop can repeat only one(first) statement

        for (int i = 0; i < 5; i++)
            System.out.println("Cydeo");
        System.out.println("WoodenSpoon");


        for (int i = 0; i < 5; i++)
            System.out.println("Batch 10");
        System.out.println("Portugal");


    }
}
