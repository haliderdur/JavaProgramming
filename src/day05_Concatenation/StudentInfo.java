package day05_Concatenation;

/*
Create a class StudentInfo and declare variables as below;
1.student name
2.age
3.gender
4.school name
5.phone number
6.full time
 */

public class StudentInfo {
    public static void main(String[] args) {
        String student_name = "Alex";
        int age = 18;
        char gender = 'M';
        String school_name = "CYDEO";
        String phone_number = "(+351) 960 359 512";
        boolean isfull_time = false;

        System.out.println("student_name = " + student_name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("school_name = " + school_name);
        System.out.println("phone_number = " + phone_number);
        System.out.println("full_time = " + isfull_time);


    }
}
